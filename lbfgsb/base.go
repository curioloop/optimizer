type iterLoc struct {
	x []float64
	f float64
	g []float64
}

func (l *iterLoc) save(x []float64, f *float64, g []float64) {
	copy(x, l.x)
	*f = l.f
	copy(g, l.g)
}

func (l *iterLoc) load(x []float64, f float64, g []float64) {
	copy(l.x, x)
	l.f = f
	copy(l.g, g)
}

